void main(){
  int number = 10;
  print("Tipo inteiro");
  print(number);
  
  double flutuante = 10.0;
  print("Tipo double");
  print(flutuante);
  
  String texto = "Algum texto aqui";
  print("Tipo String");
  print(texto);
  
  bool verdadeiro = true;
  print("Tipo Bool");
  print(verdadeiro);
  
  dynamic dinamico = 10;
  print("Tipo Dinamico");
  print(dinamico);
  dinamico = "Algum Texto 2";
  print("Tipo Dinamico");
  print(dinamico);
}